    private void btnsubmit1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnsubmit1ActionPerformed
        // TODO add your handling code here:
        String name,fatherName,sclass;
        int id;
        name=sname.getText();
        id=Integer.parseInt(roll1.getText());
        fatherName = fname.getText();
        sclass = studentclass.getText();
        
        if(!name.equals("") || Integer.toString(id).equals("") || fatherName.equals("") || sclass.equals(""))
        {
            String insertManual = "INSERT INTO class_"+sclass+"_details VALUES ('"+id+"', '"+name+"', '"+fatherName+"')";
            new Config().connect();
            try
            {
                Config.stmt2.executeUpdate(insertManual);
                JOptionPane.showMessageDialog(null, "Data Stored of "+name);
            }
            catch(Exception e)
            {
                //System.out.println("Data is Already Stored");
                e.printStackTrace();
            }
            roll1.setText("");
            studentclass.setText("");
            fname.setText("");
            sname.setText("");
            
        }
        else
        {
            JOptionPane.showMessageDialog(null, "Provide All Manual Details");
        }
    }//GEN-LAST:event_btnsubmit1ActionPerformed

