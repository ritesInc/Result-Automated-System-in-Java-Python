    private void btnsignupActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnsignupActionPerformed
        // TODO add your handling code here:
        Config conf=new Config();
        conf.connect();
        fname=txtfname.getText();
        lname=txtlname.getText();
        mail=txtmail.getText();
        mob=txtmob.getText();
        ques=cmbquestion.getItemAt(cmbquestion.getSelectedIndex());
        ans=txtans.getText();
        pass=txtpass.getText();
        if((fname.length()==0) && (lname.length()==0) && (mail.length()==0) && (mob.length()==0) && (ans.length()==0) && (pass.length()==0))
        {JOptionPane.showMessageDialog(null,"Enter Data");
        }

        String sql="Insert into Teachers values('"+fname+"','"+lname+"','"+mob+"','"+ans+"','"+pass+"','"+mail+"')";

        try {
            Config.stmt1.executeUpdate(sql);

            JOptionPane.showMessageDialog(null,"Signup successful ! Now login");
            this.dispose();
            new login().setVisible(true);

        }

        catch (Exception ex) {
            JOptionPane.showMessageDialog(null,"table not created"+ex);
        }

    }//GEN-LAST:event_btnsignupActionPerformed

